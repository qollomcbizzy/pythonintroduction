names = ['Abraham', 'Sarah', 'Mary', 'Dennis', 'Marion']
print(names)
# prints the first name in the list
print(names[0])

# prints the last name in the list
print(names[4])

# prints the second last name in the list from the back

print(names[-2])

# prints all names from the third value to the last

print(names[2:])

# prints names in between 3 and 5 except the 5

print(names[2:4])

# prints all in the list

print(names[:])

# changes the name of the list

names[1] = 'Leroy'
print(names)