# loop for a string
for item in 'Python':
    print(item)
# loop in an array
for num in [1, 2, 3, 4, 5, 6, 7]:
    print(num)

# loop using  range
for numbers in range(1, 10):
    print(numbers)

# loop odds in the number
for odd in range(1, 20, 2):
    print(odd)

# addition
prices = [10, 20, 40, 60]
total = 0
for price in prices:
    total += price
# use the formatted strong f"{}"
print("Total is:" , total)

